#Operadores Relacionales
#Se utilizan para establecer una relacion entre 2 valores
#Compara estos valores entre si y esta comparacion produce un resultado de certeza o falcedad(True o False)
#Tienen el mismo nivel de prioridad en su evaluacion
#Los operadores relacionales tienen menor prioridad que los aritmeticos

# > < >= <= != == 

resultado = 10 > 20
print(resultado)
resultado = 20 < 30
print(resultado)
resultado = 20 == 20
print(resultado)
resultado = 50 != 20
print(resultado)
resultado = 50 >= 80
print(resultado)
resultado = 50 <= 80
print(resultado)

#Los operadores aritmeticos tienen mas prioridad que los operadores relacionales
a = 20
b = 30
c = 50
resultado = a + b == c
print(resultado)











