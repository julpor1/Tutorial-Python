#Salida de datos
nombre = 'Robinson Julian Acevedo Parra'
edad = 19

#Primera forma de mostrar un mensaje por consola
print("Hola",nombre,"tienes",edad,"años")
#Segunda forma de mostrar un mensaje por consola y mas utilizada
print("Hola {} tienes {} años".format(nombre,edad))
