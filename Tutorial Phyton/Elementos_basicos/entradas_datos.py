#Entrada de datos

#La funcion input() me permite mostrar un mensaje por consola y recibir un valor digitado de tipo cadena.
nombre = input("Digite su nombre: ")
print(f"Hola {nombre} te doy la bienvenida")

#La funcion int() transforma el dato recibido de tipo cadena por la funcion input() a un dato de tipo numero
numero = int(input("Digite un numero:"))
print(f"El numero es {numero}")

#La funcion float() transforma el dato recibido de tipo cadena por la funcion input() a un dato de tipo flotante
decimal = float(input("Digite un numero decimal: "))
print(f"El numero decimal es {decimal}")









